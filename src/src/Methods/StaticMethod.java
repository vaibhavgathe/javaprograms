package Methods;

public class StaticMethod {

	public static void add()
	{
		int a = 5;
	    int b = 4;
	    
	    int add = a+b;
	    
	    System.out.println(add);
	}
	
	public static void sub()
	{
		
		int x = 10;
		int y = 20;
		int z = x-y;
		System.out.println(z);
		
	}
	
	public static void mul()
	{
		
		int x = 8;
		int y = 4;
		int z = x*y;
		
		System.out.println(z);
	}
}
